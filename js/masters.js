    // Функция открытия модального окна
    window.openMasterModal = function(masterId) {
        const master = mastersData[masterId];
        if (master && modal) {
            // Заполняем данные
            document.getElementById('masterModalName').textContent = master.name;
            document.getElementById('masterModalSpecialty').textContent = master.specialty;
            document.getElementById('masterModalDescription').innerHTML = master.description
                .map(para => para ? `<p>${para}</p>` : '<div class="master-modal-spacer"></div>')
                .join('');
            // document.getElementById('masterModalDescription').textContent = master.description;
            
            const photoImg = document.getElementById('masterModalPhoto');
            photoImg.src = master.photo;
            photoImg.alt = master.name;
            
            // Открываем модальное окно
            modal.classList.add('active');
            document.body.style.overflow = 'hidden';
        }
    };
    
    // Функция закрытия модального окна
    function closeModal() {
        if (modal) {
            modal.classList.remove('active');
            document.body.style.overflow = '';
        }
    }
    
    // Закрытие по крестику
    if (closeBtn) {
        closeBtn.addEventListener('click', closeModal);
    }
    
    // Закрытие по клику на фон
    if (modal) {
        modal.addEventListener('click', (e) => {
            if (e.target === modal) {
                closeModal();
            }
        });
    }
    
    // Закрытие по Escape
    document.addEventListener('keydown', (e) => {
        if (e.key === 'Escape' && modal && modal.classList.contains('active')) {
            closeModal();
        }
    });
